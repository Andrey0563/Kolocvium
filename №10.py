import numpy as np
import random
t = np.zeros(30, dtype=int)
for i in range(len(t)):
    t[i] = (random.randint(-15, 20))  # Заповнення масиву

k = 0
for i in range(len(t)):
    if t[i] < -10:  # Шукаємо температуру нижче -10
        k += 1
print(f'Нижче -10 градусів температура опускалась {k} разів')
