import numpy as np
import random
t = np.zeros(30, dtype=int)
for i in range(len(t)):
    t[i] = (random.randint(7, 34))  # Заповнення масиву

k = 0
for i in range(len(t)):
    if t[i] > 22:  # Шукаємо температуру придатну для купання
        k += 1
print(f'Днів придатних для купання - {k}')
